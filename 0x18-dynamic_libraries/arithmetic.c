int sum(int a,  int b)
{
	int addition = (a + b);
	return (addition);
}

int sub(int a, int b)
{
	int subtraction = (a - b);
	return (subtraction);
}

int mul(int a, int b)
{
	int product = (a * b);
	return (product);
}

int div(int a, int b)
{
	int division = (a / b);
	return (division);
}

int mod(int a, int b)
{
	int modulus = (a % b);
	return (modulus);
}
